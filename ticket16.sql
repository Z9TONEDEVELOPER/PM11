-- =====================================================
-- MySQL версия
-- =====================================================

-- Найти всех членов клуба с самой ранней датой рождения
SELECT 
    member_name
FROM ClubMembers
WHERE birth_date = (
    SELECT MIN(birth_date) 
    FROM ClubMembers
)
ORDER BY member_name;

-- =====================================================
-- Альтернативный вариант с использованием ORDER BY и LIMIT
-- (НО: этот вариант покажет только одного человека, 
-- если их несколько с одинаковой датой)
-- =====================================================

SELECT 
    member_name,
    birth_date
FROM ClubMembers
ORDER BY birth_date ASC
LIMIT 1;

-- =====================================================
-- Альтернативный вариант с подзапросом в FROM
-- (покажет всех членов с минимальной датой рождения)
-- =====================================================

SELECT cm.member_name, cm.birth_date
FROM ClubMembers cm
JOIN (
    SELECT MIN(birth_date) AS min_birth_date
    FROM ClubMembers
) m ON cm.birth_date = m.min_birth_date
ORDER BY cm.member_name;

-- =====================================================
-- PostgreSQL версия
-- =====================================================

-- Синтаксис идентичен MySQL
SELECT 
    member_name
FROM ClubMembers
WHERE birth_date = (
    SELECT MIN(birth_date) 
    FROM ClubMembers
)
ORDER BY member_name;

-- =====================================================
-- Пояснение:
-- - Подзапрос находит минимальную (самую раннюю) 
--   дату рождения
-- - Внешний запрос выбирает всех членов клуба, 
--   у которых birth_date равна найденному минимуму
-- - ORDER BY member_name - сортировка по имени 
--   для удобства просмотра
-- - Если несколько человек родились в один день 
--   (самый ранний), будут показаны все
-- =====================================================