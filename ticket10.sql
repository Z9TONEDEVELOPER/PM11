-- =====================================================
-- MySQL версия
-- =====================================================

-- Вывести все типы площадок, на которых проходили 
-- концерты в Москве, без повторов
SELECT DISTINCT venue_type
FROM Tour
WHERE town_to = 'Moscow'
ORDER BY venue_type;

-- =====================================================
-- PostgreSQL версия
-- =====================================================

-- Синтаксис идентичен MySQL
SELECT DISTINCT venue_type
FROM Tour
WHERE town_to = 'Moscow'
ORDER BY venue_type;

-- =====================================================
-- Альтернативный вариант (если нужно учитывать 
-- и город отправления, и город назначения)
-- =====================================================

-- MySQL & PostgreSQL
SELECT DISTINCT venue_type
FROM Tour
WHERE town_to = 'Moscow' OR town_from = 'Moscow'
ORDER BY venue_type;