-- Найти всех фанатов, у которых длина полного имени максимальна
-- При подсчёте длины учитываем пробелы, дефисы и точки

SELECT 
    name
FROM Fan
WHERE LENGTH(name) = (
    SELECT MAX(LENGTH(name)) 
    FROM Fan
)
ORDER BY name;

-- =====================================================
-- Альтернативный вариант с использованием ORDER BY и LIMIT
-- (но он покажет только одного фаната, если их несколько 
-- с одинаковой максимальной длиной)
-- =====================================================

SELECT 
    name,
    LENGTH(name) AS длина_имени
FROM Fan
ORDER BY LENGTH(name) DESC
LIMIT 1;

-- =====================================================
-- Альтернативный вариант с подзапросом в FROM
-- (покажет всех фанатов с максимальной длиной имени)
-- =====================================================

SELECT f.name
FROM Fan f
JOIN (
    SELECT MAX(LENGTH(name)) AS max_len
    FROM Fan
) m ON LENGTH(f.name) = m.max_len
ORDER BY f.name;

-- =====================================================
-- PostgreSQL версия
-- =====================================================

-- Синтаксис идентичен MySQL (LENGTH работает одинаково)
SELECT 
    name
FROM Fan
WHERE LENGTH(name) = (
    SELECT MAX(LENGTH(name)) 
    FROM Fan
)
ORDER BY name;

-- =====================================================
-- Пояснение:
-- - LENGTH(name) - функция для получения длины строки
-- - Подзапрос находит максимальную длину имени
-- - Внешний запрос выбирает всех фанатов, чья длина 
--   имени равна максимальной
-- - ORDER BY name - сортировка по имени для удобства
-- =====================================================