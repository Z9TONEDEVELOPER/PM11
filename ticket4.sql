-- =====================================================
-- БИЛЕТ 4 - Задание 3
-- Запрос: Найти фанатов, чьи имена заканчиваются на 'man'
-- https://sql.coderang.dev/trainer/tasks/sa-4
-- =====================================================

-- =====================================================
-- MySQL
-- =====================================================
SELECT name
FROM Fan
WHERE name LIKE '%man';

-- =====================================================
-- PostgreSQL
-- =====================================================
-- Синтаксис идентичен MySQL для данного запроса
SELECT name
FROM Fan
WHERE name LIKE '%man';

-- =====================================================
-- Альтернативный вариант с регистронезависимым поиском
-- =====================================================

-- MySQL (по умолчанию регистронезависимый)
SELECT name
FROM Fan
WHERE name LIKE '%man';

-- PostgreSQL (регистронезависимый поиск с ILIKE)
SELECT name
FROM Fan
WHERE name ILIKE '%man';

-- Или с использованием LOWER()
SELECT name
FROM Fan
WHERE LOWER(name) LIKE '%man';